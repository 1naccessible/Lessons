public class CharArithDemo {
    public static void main(String[] args) {

        char x = 'y';
        System.out.println("x contains " + x);

        x++;
        System.out.println("Now x contains " + x);

        x = 60;
        System.out.println("Now x contains " + x);

        x = 80;
        System.out.println("Now x contains " + x);

    }
}
