public class ComOpAs {
    public static void main(String[] args) {
        {int x = 10;
        System.out.println(x + " Operation x = 10");}

        {int x = 10;
        x += 2;
        System.out.println(x + " Operation x += 2");}

        {int x = 10;
        x -= 2;
        System.out.println(x + " Operation x -= 2");}

        {int x = 10;
        x /= 2;
        System.out.println(x + " Operation x /= 2");}

        {int x = 10;
        x *= 2;
        System.out.println(x + " Operation x *= 2");}

        {int x = 10;
        x %= 2;
        System.out.println(x + " Operation x %= 2");}
    }
}
